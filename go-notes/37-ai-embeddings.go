// ============================================================
// SECTION 3 — Cosine Similarity
// ============================================================
// WHY: Cosine similarity measures how similar two vectors are
// by computing the cosine of the angle between them:
//
//   cos(theta) = (A . B) / (||A|| * ||B||)
//
//   - 1.0  = identical direction (same meaning)
//   - 0.0  = orthogonal (unrelated)
//   - -1.0 = opposite direction (opposite meaning)
//
// Since we L2-normalize embeddings, cosine similarity simplifies
// to just the dot product: cos(theta) = A . B

// ──────────────────────────────────────────────────────────────
// EXAMPLE BLOCK 3 — Cosine Similarity Implementation
// ──────────────────────────────────────────────────────────────

// cosineSimilarity computes the cosine of the angle between two vectors.
//
//	cos(theta) = (A . B) / (||A|| * ||B||)
//
// where:
//
//	A . B   = sum(a_i * b_i)       — dot product
//	||A||   = sqrt(sum(a_i^2))     — L2 norm / magnitude
//
// Returns 1.0 for identical directions, 0.0 for orthogonal, -1.0 for opposite.
// If either vector is zero, returns 0.0.
func cosineSimilarity(a, b []float32) float64 {
	if len(a) != len(b) {
		panic("cosineSimilarity: vectors must have same length")
	}

	var dotProduct float64  // A . B
	var magnitudeA float64  // ||A||^2
	var magnitudeB float64  // ||B||^2

	for i := 0; i < len(a); i++ {
		ai := float64(a[i])
		bi := float64(b[i])
		dotProduct += ai * bi    // sum of a_i * b_i
		magnitudeA += ai * ai    // sum of a_i^2
		magnitudeB += bi * bi    // sum of b_i^2
	}

	magnitudeA = math.Sqrt(magnitudeA) // ||A||
	magnitudeB = math.Sqrt(magnitudeB) // ||B||

	// Guard against division by zero (zero vector)
	if magnitudeA == 0 || magnitudeB == 0 {
		return 0.0
	}

	return dotProduct / (magnitudeA * magnitudeB)
}


// ============================================================
// SECTION 7 — Embedding Applications
// ============================================================
// WHY: Embeddings aren't just for search. They power clustering
// (group similar FAQs), recommendation (suggest related content),
// deduplication (find duplicate tickets), and anomaly detection
// (flag unusual queries).

// ──────────────────────────────────────────────────────────────
// EXAMPLE BLOCK 7 — Applications: Clustering & Deduplication
// ──────────────────────────────────────────────────────────────

func demoApplications() {
	fmt.Println("============================================================")
	fmt.Println("DEMO 7: Embedding Applications")
	fmt.Println("============================================================")

	// Application 1: Deduplication — find near-duplicate FAQs
	fmt.Println("  --- Application 1: Deduplication ---")
	fmt.Println("  Finding near-duplicate FAQ entries...")
	fmt.Println()

	dupCandidates := []struct {
		textA, textB string
	}{
		{"How to cancel train ticket online", "Cancel railway ticket on IRCTC website"},
		{"Check PNR status", "PNR enquiry for train ticket"},
		{"How to cancel train ticket online", "Best restaurants near Delhi station"},
	}

	threshold := 0.65
	fmt.Printf("  Duplicate threshold: cosine > %.2f\n\n", threshold)

	for _, pair := range dupCandidates {
		vecA := simulateEmbedding(pair.textA)
		vecB := simulateEmbedding(pair.textB)
		sim := cosineSimilarity(vecA, vecB)
		isDup := sim > threshold
		dupLabel := "NOT DUPLICATE"
		if isDup {
			dupLabel = "DUPLICATE"
		}
		fmt.Printf("  '%s'\n  '%s'\n  Similarity: %.4f -> %s\n\n",
			pair.textA, pair.textB, sim, dupLabel)
	}

	// Application 2: Clustering — group FAQs by semantic topic
	fmt.Println("  --- Application 2: Semantic Clustering ---")
	fmt.Println("  Grouping FAQs by meaning (not keyword)...")
	fmt.Println()

	categories := map[string][]string{
		"Booking":      {"book train ticket", "reservation online IRCTC", "tatkal booking process"},
		"Cancellation": {"cancel ticket refund", "cancellation policy railway", "TDR filing process"},
		"PNR & Status": {"PNR status check", "train running status", "check reservation status"},
	}

	for category, texts := range categories {
		fmt.Printf("  Cluster: %s\n", category)
		// Compute centroid (average vector)
		centroid := make([]float32, embeddingDim)
		vectors := make([][]float32, len(texts))
		for i, text := range texts {
			vec := simulateEmbedding(text)
			vectors[i] = vec
			for d := range centroid {
				centroid[d] += vec[d]
			}
		}
		for d := range centroid {
			centroid[d] /= float32(len(texts))
		}

		for i, text := range texts {
			sim := cosineSimilarity(vectors[i], centroid)
			fmt.Printf("    - %q (sim to centroid: %.4f)\n", text, sim)
		}
		fmt.Println()
	}

	// Application 3: Recommendation
	fmt.Println("  --- Application 3: Recommendation ---")
	fmt.Println("  User read: 'How to book tatkal ticket'")
	fmt.Println("  Recommending related FAQs...")

	store := NewVectorStore()
	relatedFAQs := []string{
		"Tatkal booking timing and rules",
		"Difference between tatkal and premium tatkal",
		"Tatkal cancellation and refund policy",
		"How to get confirmed tatkal ticket tips",
		"Senior citizen concession on railway",
		"How to order food on train",
	}
	for i, faq := range relatedFAQs {
		vec := simulateEmbedding(faq)
		store.Insert(fmt.Sprintf("rec-%d", i), faq, vec)
	}

	queryVec := simulateEmbedding("How to book tatkal ticket")
	results := store.Search(queryVec, 4)

	fmt.Println("  Top recommendations:")
	for i, r := range results {
		fmt.Printf("    %d. [%.4f] %s\n", i+1, r.Similarity, r.Entry.Text)
	}
	fmt.Println()
}


// embeddingDim is the dimensionality of our simulated embeddings.
// Real Gemini text-embedding-004 uses 768 dimensions.
const embeddingDim = 64

// simulateEmbedding generates a deterministic embedding based on
// keywords present in the text. Related texts share keywords and
// thus produce similar vectors. This is a teaching approximation —
// real embeddings use deep neural networks with billions of params.
func simulateEmbedding(text string) []float32 {
	vec := make([]float32, embeddingDim)
	text = strings.ToLower(text)
	words := strings.Fields(text)

	// Keyword groups — words that should activate similar dimensions.
	// This mimics how a real model learns that "cancel" and "refund"
	// are semantically related.
	keywordGroups := map[string][]int{
		// Travel & railways
		"train":       {0, 1, 2},
		"railway":     {0, 1, 3},
		"ticket":      {1, 4, 5},
		"booking":     {4, 5, 6},
		"reservation": {4, 5, 7},
		"pnr":         {8, 9, 10},
		"status":      {9, 10, 11},
		"check":       {10, 11, 12},
		// Cancellation & refund
		"cancel":       {13, 14, 15},
		"cancellation": {13, 14, 16},
		"refund":       {14, 15, 17},
		"policy":       {16, 17, 18},
		// Tatkal
		"tatkal":  {19, 20, 21},
		"premium": {20, 22},
		"quota":   {21, 23},
		// Waitlist & RAC
		"waitlist":  {24, 25},
		"rac":       {25, 26},
		"confirm":   {26, 27},
		"confirmed": {26, 27},
		"berth":     {28, 29},
		"seat":      {28, 30},
		// Payment
		"payment": {31, 32},
		"pay":     {31, 33},
		"upi":     {32, 34},
		"card":    {33, 35},
		"irctc":   {36, 37},
		// General
		"how":    {38, 39},
		"what":   {39, 40},
		"get":    {40, 41},
		"find":   {41, 42},
		"help":   {42, 43},
		"number": {9, 44},
		// Food (unrelated to railways — should be distant)
		"food":   {50, 51, 52},
		"pizza":  {50, 53},
		"recipe": {51, 54},
		"cook":   {52, 55},
		"best":   {56, 57},
		"delhi":  {58, 59},
		"mumbai": {58, 60},
	}

	// Activate dimensions based on keywords found
	for _, word := range words {
		if dims, ok := keywordGroups[word]; ok {
			for _, d := range dims {
				vec[d] += 1.0
			}
		}
		// Also add a hash-based component for words not in groups
		hash := uint32(0)
		for _, ch := range word {
			hash = hash*31 + uint32(ch)
		}
		idx := int(hash % uint32(embeddingDim))
		vec[idx] += 0.3
	}

	// Normalize to unit vector (L2 normalization)
	// WHY: Cosine similarity is equivalent to dot product on unit vectors.
	// Normalizing once at creation time speeds up later searches.
	var magnitude float64
	for _, v := range vec {
		magnitude += float64(v) * float64(v)
	}
	magnitude = math.Sqrt(magnitude)
	if magnitude > 0 {
		for i := range vec {
			vec[i] = float32(float64(vec[i]) / magnitude)
		}
	}

	return vec
}


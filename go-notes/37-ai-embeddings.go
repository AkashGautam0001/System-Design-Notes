// ============================================================
// SECTION 1 — What Are Embeddings?
// ============================================================
// WHY: Before writing code, we need to understand the concept.
// An embedding is a list of numbers (a vector) that represents
// the MEANING of a piece of text. Think of it as GPS coordinates
// for meaning-space:
//
//   "train ticket"    → [0.82, 0.15, 0.91, ...]  (768 numbers)
//   "railway booking" → [0.80, 0.17, 0.89, ...]  (nearby!)
//   "pizza recipe"    → [0.12, 0.88, 0.03, ...]  (far away)
//
// ANALOGY: Imagine a giant map of India where every word has a
// location. "Mumbai" and "Bombay" are at the same spot. "Delhi"
// is nearby. "Tokyo" is far away. Embeddings do this for ALL
// text — sentences, paragraphs, documents.
//
// Key properties:
//   - Similar meanings → nearby vectors (small angle)
//   - Different meanings → distant vectors (large angle)
//   - Dimensions: typically 768 or 1536 floats
//   - Generated by models like text-embedding-004

// ──────────────────────────────────────────────────────────────
// EXAMPLE BLOCK 1 — Embedding Analogy Demonstration
// ──────────────────────────────────────────────────────────────


// ============================================================
// SECTION 6 — Batch Embedding
// ============================================================
// WHY: Embedding texts one by one is slow and wasteful. The
// Gemini API supports batch embedding — send 100 texts in one
// call and get 100 vectors back. This reduces HTTP overhead
// and is essential for initial data loading.

// ──────────────────────────────────────────────────────────────
// EXAMPLE BLOCK 6 — Batch Embedding
// ──────────────────────────────────────────────────────────────

// batchEmbed generates embeddings for multiple texts at once.
// In production: model.BatchEmbedContents(ctx, batch)
func batchEmbed(texts []string) [][]float32 {
	vectors := make([][]float32, len(texts))
	for i, text := range texts {
		vectors[i] = simulateEmbedding(text)
	}
	return vectors
}


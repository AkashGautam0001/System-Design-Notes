// ============================================================
// SECTION 4 — In-Memory Vector Store
// ============================================================
// WHY: A vector store holds embeddings and supports similarity
// search. In production, you'd use Pinecone, Weaviate, or
// pgvector. For learning (and small datasets like IRCTC FAQs),
// a simple slice-based store with brute-force search works
// perfectly and teaches the core concepts.

// ──────────────────────────────────────────────────────────────
// EXAMPLE BLOCK 4 — Vector Store Implementation
// ──────────────────────────────────────────────────────────────

// VectorEntry stores a text alongside its embedding vector.
type VectorEntry struct {
	ID     string
	Text   string
	Vector []float32
}

// SearchResult pairs a VectorEntry with its similarity score.
type SearchResult struct {
	Entry      VectorEntry
	Similarity float64
}

// VectorStore is a simple in-memory vector database.
// WHY: This is the core abstraction behind every vector DB.
// Insert stores vectors, Search finds the nearest neighbors.
type VectorStore struct {
	entries []VectorEntry
}

// NewVectorStore creates an empty vector store.
func NewVectorStore() *VectorStore {
	return &VectorStore{
		entries: make([]VectorEntry, 0),
	}
}

// Insert adds a new entry with its pre-computed embedding.
func (vs *VectorStore) Insert(id, text string, vector []float32) {
	vs.entries = append(vs.entries, VectorEntry{
		ID:     id,
		Text:   text,
		Vector: vector,
	})
}

// Search performs brute-force nearest-neighbor search.
// Returns the top-K most similar entries to the query vector.
//
// Algorithm: O(n) scan — compute similarity with every stored vector,
// sort by similarity descending, return top K.
//
// For production scale (millions of vectors), use approximate nearest
// neighbor (ANN) algorithms: HNSW, IVF, or ScaNN.
func (vs *VectorStore) Search(queryVec []float32, topK int) []SearchResult {
	results := make([]SearchResult, 0, len(vs.entries))

	for _, entry := range vs.entries {
		sim := cosineSimilarity(queryVec, entry.Vector)
		results = append(results, SearchResult{
			Entry:      entry,
			Similarity: sim,
		})
	}

	// Sort by similarity descending
	sort.Slice(results, func(i, j int) bool {
		return results[i].Similarity > results[j].Similarity
	})

	// Return top K
	if topK > len(results) {
		topK = len(results)
	}
	return results[:topK]
}

// Size returns the number of entries in the store.
func (vs *VectorStore) Size() int {
	return len(vs.entries)
}

